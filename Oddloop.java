class Oddloop
  {
    public static void main(String args[])
    {
      int number=10;
      System.out.println("list of odd numbers from 1 to "+number+":");
      for(int i=1; i<=number; i++)
        {
          if(i==0)
          {
            System.out.println(i);
          }
        }
    }
  }